SELECT TOP 50 PERCENT * FROM WORKER; 

(B)
SELECT * FROM WORKER
INNER JOIN BONUS ON WORKER.ID = BONUS.WORKER_ID 
INNER JOIN DEPARTMENT ON WORKER.ID = DEPARTMENT.WORKER_REF_ID;

(C)
SELECT DEPARTMENT,COUNT(ID)
FROM WORKER
GROUP BY DEPARTMENT
HAVING COUNT(ID) < 5;

(D)
SELECT DEPARTMENT,CONCAT(FIRST_NAME,' ',LAST_NAME), MAX(SALARY)
FROM WORKER
GROUP BY DEPARTMENT; 

(E)
SELECT MAX(SALARY) AS Salary 
FROM WORKER; 
